<?php



namespace {

	/**
	 * The class name. The class name includes the namespace it was declared in (e.g. <i>Foo\Bar</i>). Note that as of PHP 5.4 __CLASS__ works also in traits. When used in a trait method, __CLASS__ is the name of the class the trait is used in.
	 */
	define('__CLASS__', null);

	/**
	 * The directory of the file. If used inside an include, the directory of the included file is returned. This is equivalent to <i>dirname(__FILE__)</i>. This directory name does not have a trailing slash unless it is the root directory.
	 */
	define('__DIR__', null);

	/**
	 * The full path and filename of the file with symlinks resolved. If used inside an include, the name of the included file is returned.
	 */
	define('__FILE__', null);

	/**
	 * The function name.
	 */
	define('__FUNCTION__', null);

	/**
	 * The current line number of the file.
	 */
	define('__LINE__', null);

	/**
	 * The class method name.
	 */
	define('__METHOD__', null);

	/**
	 * The name of the current namespace.
	 */
	define('__NAMESPACE__', null);

	/**
	 * The trait name. The trait name includes the namespace it was declared in (e.g. <i>Foo\Bar</i>).
	 */
	define('__TRAIT__', null);

}
